// IL Calculator for concentrated liquidity
// Formula: IL ≈ (priceRatio - 1)^2 / (8 * rangeWidth) for small moves
// But more accurately, we track exact IL for given price move
export function calculateIL(priceChangePct, rangeWidthPct) {
  // For concentrated liquidity in range [P/(1+r), P*(1+r)]
  // where r = rangeWidthPct
  // If price moves by x%, the IL is:
  const r = rangeWidthPct;
  const x = priceChangePct / 100;
  
  if (Math.abs(x) >= r) {
    // Price outside range = 100% in one token
    // For CL when price moves beyond range: we hold only the depreciating asset
    // Rough IL estimate: ~50% of the price move percentage
    return Math.abs(x) * 50; // e.g., 5% move = ~2.5% IL
  }
  
  // In-range IL (simplified Uniswap v3 formula)
  // For concentrated liquidity in range [1/(1+r), (1+r)]:
  // IL ≈ (x^2) / (6r) for small moves within range
  const il = (x * x) / (6 * r) * 100;
  return il;
}

// Fee income calculator
export function calculateFeeIncome({
  capitalUsd,
  poolTvlUsd,
  dailyVolumeUsd,
  feeTierPct,
  rangeWidthPct, // e.g., 0.02 for ±2%
  days = 30,
}) {
  // Capital efficiency multiplier for concentrated liquidity
  // Full range = 1x, ±r% range ≈ 1/(2r)
  const capitalEfficiency = 1 / (2 * rangeWidthPct);
  
  // Our effective share of liquidity
  const effectiveShare = (capitalUsd / poolTvlUsd) * capitalEfficiency;
  
  // Daily fees generated by pool
  const dailyPoolFees = dailyVolumeUsd * (feeTierPct / 100);
  
  // Our daily fee income
  const ourDailyFees = dailyPoolFees * effectiveShare;
  
  // Period income
  const periodIncome = ourDailyFees * days;
  const apy = (periodIncome / capitalUsd) * (365 / days) * 100;
  
  return {
    capitalEfficiency,
    effectiveShare: effectiveShare * 100, // as pct
    dailyPoolFees,
    ourDailyFees,
    periodIncome,
    apy,
  };
}

// Rebalancing cost calculator
export function calculateRebalanceCost({
  gasCostUsd = 0.05, // Base
  swapSlippagePct = 0.1,
  capitalUsd,
  priceChangePct, // How much price moved
  rangeWidthPct = 0.02,
}) {
  // Gas for remove liquidity + swap + add liquidity
  const gasTotal = gasCostUsd * 3; // 3 transactions
  
  // Swap cost to rebalance back to 50/50
  const halfCapital = capitalUsd / 2;
  const swapVolume = halfCapital * (Math.abs(priceChangePct) / 100);
  const swapCost = swapVolume * (swapSlippagePct / 100);
  
  // IL from the move itself (use actual range width)
  const ilCost = capitalUsd * (calculateIL(priceChangePct, rangeWidthPct) / 100);
  
  return {
    gasCost: gasTotal,
    swapCost,
    ilCost,
    totalCost: gasTotal + swapCost + ilCost,
  };
}

// Strategy simulator
export function simulateCLStrategy({
  pool,
  capitalUsd,
  rangeWidthPct = 0.02, // ±2%
  holdDays = 30,
  pricePath = [], // Array of daily ETH price changes in pct
  rebalanceThresholdPct = null, // Auto: 80% of range width
} = {}) {
  // Auto-set rebalance threshold: rebalance when price has moved 80% toward edge
  const actualThreshold = rebalanceThresholdPct ?? (rangeWidthPct * 100 * 0.8);

  // Default price path: ETH volatility simulation
  if (pricePath.length === 0) {
    // Simulate moderate volatility: daily moves -2% to +2%
    for (let i = 0; i < holdDays; i++) {
      pricePath.push((Math.random() - 0.5) * 4); // -2% to +2%
    }
  }
  
  const feeCalc = calculateFeeIncome({
    capitalUsd,
    poolTvlUsd: pool.tvlUsd,
    dailyVolumeUsd: pool.dailyVolumeUsd,
    feeTierPct: pool.feeTier,
    rangeWidthPct,
    days: holdDays,
  });
  
  // Simulate rebalancing
  let cumulativePriceChange = 0;
  let rebalances = 0;
  let totalRebalanceCost = 0;
  let inRangeDays = 0;
  let outOfRangeDays = 0;
  
  for (let day = 0; day < holdDays; day++) {
    const dailyMove = pricePath[day];
    cumulativePriceChange += dailyMove;
    
    // Check if still in range
    if (Math.abs(cumulativePriceChange) < rangeWidthPct * 100) {
      inRangeDays++;
    } else {
      outOfRangeDays++;
    }
    
    // Rebalance trigger (use auto-calculated threshold)
    if (Math.abs(cumulativePriceChange) >= actualThreshold) {
      const cost = calculateRebalanceCost({
        capitalUsd,
        priceChangePct: cumulativePriceChange,
        rangeWidthPct,
      });
      totalRebalanceCost += cost.totalCost;
      rebalances++;
      cumulativePriceChange = 0; // Reset after rebalance
    }
  }
  
  // Final rebalance if needed at end
  if (Math.abs(cumulativePriceChange) > 0.5) {
    const cost = calculateRebalanceCost({
      capitalUsd,
      priceChangePct: cumulativePriceChange,
      rangeWidthPct,
    });
    totalRebalanceCost += cost.totalCost;
    rebalances++;
  }
  
  // Effective fee income (only when in range)
  const timeInRange = inRangeDays / holdDays;
  const effectiveFeeIncome = feeCalc.periodIncome * timeInRange;
  
  const netProfit = effectiveFeeIncome - totalRebalanceCost;
  const netApy = (netProfit / capitalUsd) * (365 / holdDays) * 100;
  
  return {
    pool: pool.pair,
    capitalUsd,
    rangeWidthPct,
    holdDays,
    timeInRange: timeInRange * 100,
    rebalances,
    grossFeeIncome: effectiveFeeIncome,
    totalRebalanceCost,
    netProfit,
    netApy,
    breakdown: {
      feeCalc,
      inRangeDays,
      outOfRangeDays,
    },
  };
}

