// Entry/Exit policy
export async function evaluateCLEntry({
  pool,
  capitalUsd,
  currentEthPrice,
  volatility24h, // ETH 24h volatility in pct
  trendDirection, // 'up', 'down', 'sideways'
}) {
  const blockers = [];
  
  // Minimum capital
  if (capitalUsd < 200) {
    blockers.push("insufficient_capital_for_cl:need_200usd_min");
  }
  
  // Volatility check: too volatile = high IL risk
  if (volatility24h > 8) {
    blockers.push("volatility_too_high_for_narrow_range");
  }
  
  // Trending market is bad for CL
  if (trendDirection !== 'sideways' && volatility24h > 5) {
    blockers.push("trending_market_high_il_risk");
  }
  
  // TVL check
  if (pool.tvlUsd < 1_000_000) {
    blockers.push("insufficient_tvl_for_safe_exit");
  }
  
  // Calculate expected return
  const sim = simulateCLStrategy({ pool, capitalUsd, holdDays: 7 });
  if (sim.netApy < 15) {
    blockers.push("expected_return_below_15pct_apy");
  }
  
  return {
    decision: blockers.length === 0 ? "ALLOW" : "BLOCK",
    blockers,
    expectedNetApy: sim.netApy,
    recommendation: blockers.length === 0 
      ? `Enter ${pool.pair} CL position with $${capitalUsd}, ±2% range. Expected ${sim.netApy.toFixed(1)}% APY.`
      : `Blocked: ${blockers.join(', ')}`,
  };
}

export async function evaluateCLExit({
  position,
  currentIlPct,
  accumulatedFeesUsd,
  daysHeld,
}) {
  const blockers = [];
  
  // Exit if IL > 3x accumulated fees
  if (currentIlPct > 0 && accumulatedFeesUsd > 0) {
    const ilUsd = position.capitalUsd * (currentIlPct / 100);
    if (ilUsd > accumulatedFeesUsd * 3) {
      blockers.push("il_exceeds_3x_fees:exit_immediately");
    }
  }
  
  // Exit if out of range for >3 days
  if (position.outOfRangeDays > 3) {
    blockers.push("out_of_range_3_days:rebalance_or_exit");
  }
  
  // Exit if net APY turns negative
  const netReturn = accumulatedFeesUsd - (position.capitalUsd * currentIlPct / 100);
  const netApy = (netReturn / position.capitalUsd) * (365 / Math.max(daysHeld, 1)) * 100;
  if (netApy < 0) {
    blockers.push("negative_net_apy:exit_to_preserve_capital");
  }
  
  return {
    decision: blockers.length > 0 ? "EXIT" : "HOLD",
    blockers,
    netApy,
    recommendation: blockers.length > 0
      ? `Exit ${position.pool.pair}. Reasons: ${blockers.join(', ')}`
      : `Hold ${position.pool.pair}. Net APY: ${netApy.toFixed(1)}%`,
  };
}

// Main intent builder
export function buildCLIntent({
  action, // 'enter', 'rebalance', 'exit'
  poolKey,
  capitalUsd,
  rangeWidthPct = 0.02,
  walletAddress,
}) {
  const pool = AERODROME_POOLS[poolKey];
  if (!pool) throw new Error(`Unknown pool: ${poolKey}`);
  
  const halfCapital = capitalUsd / 2;
  const token0Amount = halfCapital / ETH_PRICE_USD; // WETH
  const token1Amount = halfCapital; // USDC
  
  return {
    schemaVersion: 1,
    intentType: "concentrated_liquidity",
    action,
    strategyId: `cl-${poolKey}`,
    chain: pool.chain,
    pool: pool.poolAddress,
    pair: pool.pair,
    capitalUsd,
    rangeWidthPct,
    token0: {
      symbol: pool.token0,
      address: pool.token0Address,
      amount: action === 'enter' ? token0Amount : undefined,
    },
    token1: {
      symbol: pool.token1,
      address: pool.token1Address,
      amount: action === 'enter' ? token1Amount : undefined,
    },
    walletAddress,
    estimatedGasUsd: action === 'enter' ? 0.15 : action === 'rebalance' ? 0.15 : 0.05,
    observedAt: new Date().toISOString(),
  };
}

